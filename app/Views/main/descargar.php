    <script>
        // --- Mobile menu toggle ---
        const mobileMenuButton = document.getElementById('mobile-menu-button');
        const mobileMenu = document.getElementById('mobile-menu');
        mobileMenuButton.addEventListener('click', () => {
            mobileMenu.classList.toggle('hidden');
        });

        // --- Scroll Animations ---
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('visible');
                }
            });
        }, { threshold: 0.1 });

        document.querySelectorAll('.reveal').forEach(el => {
            observer.observe(el);
        });

        // --- Team Member Description Toggle ---
        document.querySelectorAll('.team-member button').forEach(button => {
            button.addEventListener('click', (e) => {
                const memberDiv = e.target.closest('.team-member');
                document.querySelectorAll('.team-member').forEach(m => {
                    if (m !== memberDiv) {
                        m.classList.remove('active');
                        m.querySelector('.description').style.maxHeight = null;
                        m.querySelector('.description').style.opacity = 0;
                    }
                });
                
                memberDiv.classList.toggle('active');
                const description = memberDiv.querySelector('.description');
                if (memberDiv.classList.contains('active')) {
                    description.style.maxHeight = description.scrollHeight + "px";
                    description.style.opacity = 1;
                } else {
                    description.style.maxHeight = null;
                     description.style.opacity = 0;
                }
            });
        });

        // --- Chatbot ---
        const chatbotWindow = document.getElementById('chatbot-window');
        function toggleChatbot() {
            chatbotWindow.classList.toggle('hidden');
            chatbotWindow.classList.toggle('flex');
        }

        function sendMessage() {
            const input = document.getElementById('user-input');
            const chatBody = document.getElementById('chat-body');
            const message = input.value.trim();

            if (message) {
                // Add user message
                const userMsg = document.createElement('div');
                userMsg.className = 'flex justify-end mb-3 chat-bubble';
                userMsg.innerHTML = `<div class="bg-primary text-white p-3 rounded-lg max-w-xs shadow">${message}</div>`;
                chatBody.appendChild(userMsg);
                setTimeout(() => userMsg.classList.add('visible'), 50);


                input.value = '';
                chatBody.scrollTop = chatBody.scrollHeight;
                
                // Bot response placeholder
                setTimeout(() => {
                    const botMsg = document.createElement('div');
                    botMsg.className = 'flex justify-start mb-3 chat-bubble';
                    botMsg.innerHTML = `<div class="bg-background-light text-text-dark p-3 rounded-lg max-w-xs shadow">Gracias por tu mensaje. Un especialista se pondrá en contacto contigo pronto.</div>`;
                    chatBody.appendChild(botMsg);
                    setTimeout(() => botMsg.classList.add('visible'), 50);
                    chatBody.scrollTop = chatBody.scrollHeight;
                }, 1200);
            }
        }
        document.getElementById('user-input').addEventListener('keypress', function (e) {
            if (e.key === 'Enter') {
                sendMessage();
            }
        });

         // --- Dije/App Toggle Script ---
        const dijeBtn = document.getElementById('dije-btn');
        const appBtn = document.getElementById('app-btn');
        const dijeContent = document.getElementById('dije-content');
        const appContent = document.getElementById('app-content');

        if(dijeBtn && appBtn && dijeContent && appContent) {
            dijeBtn.addEventListener('click', () => {
                dijeBtn.classList.add('active');
                dijeBtn.classList.remove('inactive');
                appBtn.classList.add('inactive');
                appBtn.classList.remove('active');

                dijeContent.classList.remove('hidden');
                appContent.classList.add('hidden');
            });

            appBtn.addEventListener('click', () => {
                appBtn.classList.add('active');
                appBtn.classList.remove('inactive');
                dijeBtn.classList.add('inactive');
                dijeBtn.classList.remove('active');

                appContent.classList.remove('hidden');
                dijeContent.classList.add('hidden');
            });
        }
    </script>
